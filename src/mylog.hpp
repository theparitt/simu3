#ifndef MYLOG_HPP_
#define MYLOG_HPP_


#include <iostream>

#define LOG(S)           (mylog::d(#S))
#define LOG_I(S)         (mylog::info(#S, __LINE__, __FILE__ ))
#define LOG_E(S,b)       (mylog::e(#S,b))


#define DUMP(varname) \
		fprintf(stderr, "%s = %x", #varname, varname)

#define TRACE(fmt, var) \
        (error_at_line(0, 0, __FILE__, __LINE__, "%s : " fmt, #var, var))

class mylog
{

public:

	template <typename T>
	static void d(T& vari,const char* debug_msg="" )
	{
		std::cout << debug_msg << vari << std::endl;
		fflush( stdout );
	}



	static void info(const char* debug_msg, int line, char* file  )
	{
		printf("%s         (%s:%d)\n", debug_msg, file, line );
		fflush( stdout );
	}


	static void e(const char* debug_msg, bool cond )
	{
		if( cond )
		{
			fprintf(stderr, "%s\n", debug_msg );
			fflush( stderr );
			getchar();

		}
	}


};



#endif /* MYLOG_HPP_ */




